    private void atualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizarActionPerformed
        try {
            Comunicacao comunic = new Comunicacao(1);
            listaSalas = c.getSalas(comunic);
            DefaultTableModel tabela = (DefaultTableModel) salas.getModel();
            
            if (listaSalas == null) {
                JOptionPane.showMessageDialog(null, "N√£o foi poss√≠vel se conectar com o servidor!", "Erro", JOptionPane.ERROR_MESSAGE);
                disableButtons(false);
            }else if (listaSalas.isEmpty()) {
                tabela.setRowCount(0);
                JOptionPane.showMessageDialog(null, "N√£o existe nenuma sala criada! Crie uma para jogar!", "Salas de Jogo", JOptionPane.INFORMATION_MESSAGE);
                disableButtons(true);
            }else{
                disableButtons(true);
                tabela.setRowCount(0); //Limpa dados inseridos na tabela de salas para atualizar com os novos recebidos do servidor.
                
                Iterator itr = listaSalas.iterator();
                while (itr.hasNext()) {
                    Sala s = (Sala) itr.next();
                    tabela.addRow(s.stringInfo());
                }
            }
        } catch (ClassNotFoundException ex) {
        }
    }//GEN-LAST:event_atualizarActionPerformed

    private void getSalas(){
        Comunicacao comunic = new Comunicacao(1);
        
        new Thread(){
            LinkedList<Sala> listaSalas = null;
            DefaultTableModel tabela = (DefaultTableModel) salas.getModel();
            
            public void run(){
                do {
                    try {
                        listaSalas = c.getSalas(comunic);
                        if (listaSalas == null) {
                            disableButtons(false);
                        }else if (listaSalas.isEmpty()) {
                            JOptionPane.showMessageDialog(null, "N√£o existe nenuma sala criada! Crie uma para jogar!", "Salas de Jogo", JOptionPane.INFORMATION_MESSAGE);
                            disableButtons(true);
                            tabela.setRowCount(0);
                        }else{
                            disableButtons(true);
                            
                            tabela.setRowCount(0); //Limpa dados inseridos na tabela de salas para atualizar com os novos recebidos do servidor.
                            
                            Iterator itr = listaSalas.iterator();
                            while (itr.hasNext()) {
                                Sala s = (Sala) itr.next();
                                tabela.addRow(s.stringInfo());
                            }
                        }
                    } catch (ClassNotFoundException ex) {
                        Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } while (listaSalas == null);
                
            }
        }.start();
    }
    
